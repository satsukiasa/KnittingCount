import UIKit

extension UIColor {
    class var countGray: UIColor { return #colorLiteral(red: 0.5333333333, green: 0.5333333333, blue: 0.5333333333, alpha: 1) }
    class var countRed: UIColor { return #colorLiteral(red: 1, green: 0.4, blue: 0.4, alpha: 1) }
}
